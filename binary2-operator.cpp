#include<iostream>
using namespace std;

int main()
{
    int vencha = 15;
    int ibzan = 3;

    int group1 = 0;

    //Addition
    group1 = vencha + ibzan ;
    cout << "Addition" << " : " << group1 <<"\n";

    //Multiplication
    group1 = vencha * ibzan ;
    cout << "Multiplication" << " : " << group1 <<"\n";

    //Divison
    group1 = vencha / ibzan;
    cout << "Divison" << " : " <<group1 <<"\n";

    //Subtraction
    group1 = vencha - ibzan;
    cout << "Subtraction" << " : " << group1 <<"\n";

return 0;
}
